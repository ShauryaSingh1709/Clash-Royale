    print(f"Total decks: {stats['total']}")
    print(f"\nBy Tier:")
    for tier, count in stats['tiers'].items():
        print(f"  {tier}-Tier: {count}")
    print(f"\nBy Archetype:")
    for arch, count in stats['archetypes'].items():
        print(f"  {arch}: {count}")
def get_decks_by_tier(tier: str) -> list:

    return [d for d in REAL_META_DECKS if d["tier"] == tier]


def get_decks_by_archetype(archetype: str) -> list:

    return [d for d in REAL_META_DECKS if d["archetype"] == archetype]


def get_stats() -> dict:

    return {
        "total": len(REAL_META_DECKS),
        "tiers": {
            "S": len(get_decks_by_tier("S")),
            "A": len(get_decks_by_tier("A")),
            "B": len(get_decks_by_tier("B")),
            "C": len(get_decks_by_tier("C")),
        },
        "archetypes": {
            "Beatdown": len(get_decks_by_archetype("Beatdown")),
            "Control": len(get_decks_by_archetype("Control")),
            "Cycle": len(get_decks_by_archetype("Cycle")),
            "Siege": len(get_decks_by_archetype("Siege")),
        }
    }


if __name__ == "__main__":
    stats = get_stats()
